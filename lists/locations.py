locations = ["venice", "italy", "russia", "egypt", "miami"]
print(locations)

# sort the list temprorily
print(sorted(locations))

print(locations)

# sort the list in reverse order temprorily
print(sorted(locations))

print(locations)

# to reverse the order of list permanently
locations.reverse()
print(locations)

# to reverse the order of list permanently
print(locations.reverse())
print(locations)

# to sort the list permanently 
print(locations.sort())
print(locations)

# to sort the list in reverse order permanently 
print(locations.sort(reverse=True))
print(locations)


